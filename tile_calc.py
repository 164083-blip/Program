import math

print("--- โปรแกรมคำนวณจำนวนกระเบื้อง (Tile Calculator) ---")

# 1. รับค่าขนาดพื้นที่ห้อง (หน่วย: เมตร)
กว้าง_ห้อง = float(input("ระบุความกว้างของห้อง (เมตร): "))
ยาว_ห้อง = float(input("ระบุความยาวของห้อง (เมตร): "))

พื้นที่_ห้อง = กว้าง_ห้อง * ยาว_ห้อง
print(f"-> พื้นที่ห้องทั้งหมด: {พื้นที่_ห้อง:.2f} ตารางเมตร")
print("-" * 40)

# 2. รับค่าขนาดกระเบื้อง (หน่วย: เซนติเมตร)
# เช่น กระเบื้องขนาด 30x30 ซม. หรือ 60x60 ซม.
กว้าง_กระเบื้อง = float(input("ระบุความกว้างของกระเบื้อง (เซนติเมตร): "))
ยาว_กระเบื้อง = float(input("ระบุความยาวของกระเบื้อง (เซนติเมตร): "))

# แปลงขนาดกระเบื้องจาก เซนติเมตร ให้เป็น เมตร ก่อนคำนวณพื้นที่
พื้นที่_กระเบื้อง_ต่อแผ่น = (กว้าง_กระเบื้อง / 100) * (ยาว_กระเบื้อง / 100)

print("-" * 40)

# 3. คำนวณจำนวนกระเบื้องที่ต้องใช้
# ใช้ math.ceil() เพื่อปัดเศษขึ้นเสมอ (เพราะกระเบื้องขายเป็นแผ่นเต็ม)
จำนวนกระเบื้อง_พอดี = math.ceil(พื้นที่_ห้อง / พื้นที่_กระเบื้อง_ต่อแผ่น)

# เผื่อกระเบื้องเสียหายจากการตัดหรือทำแตก (แนะนำที่ 10%)
จำนวนกระเบื้อง_เผื่อเหลือ = math.ceil(จำนวนกระเบื้อง_พอดี * 1.10)

# 4. แสดงผลลัพธ์
print("--- สรุปผลการคำนวณ ---")
print(f"จำนวนกระเบื้องที่ต้องใช้จริง: {จำนวนกระเบื้อง_พอดี} แผ่น")
print(f"จำนวนที่ควรซื้อจริง (เผื่อเหลือเศษ 10%): {จำนวนกระเบื้อง_เผื่อเหลือ} แผ่น")